     */
    public function update(Request $request, Vehicle $vehicle)
    {
        $validated = $request->validate([
            'engine' => ['required', 'max:255'],
            'plate' => ['required', 'alpha_dash'],
        ]);

        if (!$validated) {
            return back()->withErrors($validated);
        }

        $vehicle->update($request->all());

        return back();
    }

